// Rest duration guidance by exercise type and training goal
// Returns seconds of rest recommended
export function getRestDuration(exerciseId, exerciseType, energy = 'medium') {
  const restMap = {
    // Compound / heavy lifts — longer rest
    barbell_squat: 120,
    barbell_deadlift: 150,
    barbell_bench: 120,
    barbell_ohp: 120,
    barbell_row: 90,
    machine_leg_press: 90,
    // Dumbbell compound — moderate rest
    db_goblet_squat: 75,
    db_deadlift: 90,
    db_chest_press: 75,
    db_lunge: 75,
    db_rdl: 90,
    // Isolation / lighter — shorter rest
    db_bicep_curl: 60,
    db_lateral_raise: 60,
    db_tricep_extension: 60,
    // Machine / cable — moderate
    machine_lat_pulldown: 75,
    machine_row: 75,
    machine_chest_press: 75,
    cable_tricep_pushdown: 60,
    cable_face_pull: 60,
    cable_fly: 60,
    // Bodyweight compounds — moderate
    pullup: 90,
    dip: 90,
    // Cardio / circuits — very short or no rest
    burpees: 30,
    high_knees: 20,
    mountain_climbers: 20,
    treadmill_sprint: 30,
    bear_crawl: 30,
  }

  const base = restMap[exerciseId] || 60

  // Adjust by energy — lower energy = slightly shorter rest (faster recovery)
  const energyMult = { low: 0.85, medium: 1.0, high: 1.15 }
  return Math.round(base * (energyMult[energy] || 1.0))
}

